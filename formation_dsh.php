        <h2 class="text-uppercase">Le Métier</h2>
        <p>
            Le/la Data Security Helper (DSH) travaille dans tous types d’environnements professionnels (grandes entreprises privées ou publiques, PME, PMI, société de services en ingénierie informatique (SSII), entreprise de service du numérique (ESN), société de services en télécoms et réseaux (SSTR), seul ou en équipe, généralement sous la supervision d’un ingénieur réseau ou d’un RSSI. Son activité s’inscrit dans le respect des consignes, procédures, normes nationales et européennes et les contrats de services.
        </p>

        <p>Acteur / trice de la sécurisation des données et des ﬂux, dans le cadre, entre autres, du respect du nouveau Règlement Général sur la protection des données, le/la DSH est en capacité de relever les tentatives d’intrusions depuis internet, analyser les échanges et appliquer la politique de sécurité de l’entreprise. Il/elle agit entre autres auprès des utilisateurs et usagers de l’entreprise pour les sensibiliser à la sécurité informatique, assure une veille active sur les menaces aﬁn d’envisager des plans d’actions pour éviter la contamination directe ou collatérale. Il/elle intervient dans le paramétrage des outils de communication, de sauvegarde et de partage des données (cloud) en interne ou avec l’extérieur (mails, outils collaboratifs…).
        </p>

        <p>
            Sur un plan infrastructure, il/elle met en service, maintien à jour, assure la maintenance préventive, le diagnostic et la résolution d’incidents simples et complexes des équipements et des logiciels de réseaux informatiques et de télécommunications.
        </p>

        <p>
            Il/elle sera amené(e) à analyser le cahier des charges des besoins clients en matière de systèmes réseaux et télécommunications pour en assurer l’intégration, l’installation, la mise en service, la conﬁguration, la sécurisation, le dépannage, la maintenance et les mises à jour.
        </p>

        <p>
            Il/elle pourra être amené(e) à utiliser des outils d’analyse d’incident ou de remontée d’alarme pour identiﬁer et dépanner les systèmes, proposer des solutions de contournement pour maintenir la production en allant jusqu’à l’audit approfondi aﬁn de contribuer à l’évolution du réseau.
        </p>

        <p>
            Doté/e de bonnes connaissances en gestion de projet technique, le/la DSH est en capacité de suivre et de mettre à jour les tableaux de bord du réseau (indicateurs de performance, ﬂux et qualité de service) et de s’astreindre à une organisation rigoureuse de façon à intervenir simultanément sur plusieurs tâches tout en respectant les délais exigés. Sa connaissance de l’anglais lui permet d’exploiter des documents techniques et d’échanger dans certains environnements (forum, web conférences…) avec des interlocuteurs étrangers.
        </p>

